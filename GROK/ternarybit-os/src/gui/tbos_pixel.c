void tbos_pixbuf_blit(tbos_pixbuf_t* dst, int dx, int dy,
                      const tbos_pixbuf_t* src, int sx, int sy,
                      int w, int h) {
    if (!dst || !src) return;

    for (int y = 0; y < h; y++) {
        for (int x = 0; x < w; x++) {
            tbos_rgba_t c = tbos_pixbuf_get(src, sx + x, sy + y);
            tbos_pixbuf_set(dst, dx + x, dy + y, c);
        }
    }
}

void tbos_pixbuf_blit_blend(tbos_pixbuf_t* dst, int dx, int dy,
                            const tbos_pixbuf_t* src, int sx, int sy,
                            int w, int h) {
    if (!dst || !src) return;

    for (int y = 0; y < h; y++) {
        for (int x = 0; x < w; x++) {
            tbos_rgba_t c = tbos_pixbuf_get(src, sx + x, sy + y);
            tbos_pixbuf_blend(dst, dx + x, dy + y, c);
        }
    }
}

tbos_pixbuf_t* tbos_pixbuf_scale(const tbos_pixbuf_t* src,
                                  uint32_t new_width, uint32_t new_height) {
    if (!src) return NULL;

    tbos_pixbuf_t* dst = tbos_pixbuf_create(new_width, new_height, src->format);
    if (!dst) return NULL;

    /* Nearest-neighbor scaling */
    for (uint32_t y = 0; y < new_height; y++) {
        for (uint32_t x = 0; x < new_width; x++) {
            int sx = x * src->width / new_width;
            int sy = y * src->height / new_height;
            tbos_rgba_t c = tbos_pixbuf_get(src, sx, sy);
            tbos_pixbuf_set(dst, x, y, c);
        }
    }

    return dst;
}
