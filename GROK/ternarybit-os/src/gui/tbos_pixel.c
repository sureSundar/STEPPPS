/* Blit a glyph's bitmap rows into the buffer, alpha-blending each set bit
 * against the target so glyphs composite correctly over translucent panels. */
void tbos_draw_char(tbos_pixbuf_t* buf, int x, int y, char c,
                    const tbos_font_t* font, tbos_rgba_t color) {
    if (!buf || !font || !font->data) return;

    unsigned char uc = (unsigned char)c;
    if (uc < font->first_char || uc > font->last_char) {
        uc = '?';
        if (uc < font->first_char || uc > font->last_char) return;
    }

    const uint8_t* glyph = font->data + (size_t)(uc - font->first_char) * font->height;

    for (int row = 0; row < font->height; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < font->width; col++) {
            if (bits & (0x80 >> col)) {
                tbos_pixbuf_blend(buf, x + col, y + row, color);
            }
        }
    }
}

void tbos_draw_text(tbos_pixbuf_t* buf, int x, int y, const char* text,
                    const tbos_font_t* font, tbos_rgba_t color) {
    if (!buf || !text || !font) return;

    int cx = x;
    while (*text) {
        if (*text == '\n') {
            cx = x;
            y += font->height;
        } else {
            tbos_draw_char(buf, cx, y, *text, font, color);
            cx += font->width;
        }
        text++;
    }
}

int tbos_text_width(const char* text, const tbos_font_t* font) {
    if (!text || !font) return 0;

    int max_width = 0;
    int width = 0;

    while (*text) {
        if (*text == '\n') {
            if (width > max_width) max_width = width;
            width = 0;
        } else {
            width += font->width;
        }
        text++;
    }

    return width > max_width ? width : max_width;
}

void tbos_draw_text_centered(tbos_pixbuf_t* buf, int x, int y, int w,
                              const char* text, const tbos_font_t* font,
                              tbos_rgba_t color) {
    int tw = tbos_text_width(text, font);
    int cx = x + (w - tw) / 2;
    tbos_draw_text(buf, cx, y, text, font, color);
}

