static size_t bytes_per_pixel(tbos_pixel_format_t format) {
    switch (format) {
        case TBOS_PIXEL_1BIT:    return 0;  /* Special: 8 pixels per byte */
        case TBOS_PIXEL_4BIT:    return 0;  /* Special: 2 pixels per byte */
        case TBOS_PIXEL_8BIT:    return 1;
        case TBOS_PIXEL_RGB565:  return 2;
        case TBOS_PIXEL_RGB24:   return 3;
        case TBOS_PIXEL_RGBA32:  return 4;
        default:                 return 4;
    }
}

tbos_pixbuf_t* tbos_pixbuf_create(uint32_t width, uint32_t height,
                                   tbos_pixel_format_t format) {
    tbos_pixbuf_t* buf = calloc(1, sizeof(tbos_pixbuf_t));
    if (!buf) return NULL;

    buf->width = width;
    buf->height = height;
    buf->format = format;
    buf->owned = true;

    size_t bpp = bytes_per_pixel(format);
    if (bpp == 0) {
        /* Bit-packed formats */
        if (format == TBOS_PIXEL_1BIT) {
            buf->stride = (width + 7) / 8;
        } else if (format == TBOS_PIXEL_4BIT) {
            buf->stride = (width + 1) / 2;
        }
    } else {
        buf->stride = width * bpp;
    }

    buf->data = calloc(height, buf->stride);
    if (!buf->data) {
        free(buf);
        return NULL;
    }

    return buf;
}

tbos_pixbuf_t* tbos_pixbuf_from_data(uint8_t* data, uint32_t width,
                                      uint32_t height, uint32_t stride,
                                      tbos_pixel_format_t format) {
    tbos_pixbuf_t* buf = calloc(1, sizeof(tbos_pixbuf_t));
    if (!buf) return NULL;

    buf->data = data;
    buf->width = width;
    buf->height = height;
    buf->stride = stride;
    buf->format = format;
    buf->owned = false;

    return buf;
}

void tbos_pixbuf_free(tbos_pixbuf_t* buf) {
    if (!buf) return;
    if (buf->owned && buf->data) {
        free(buf->data);
    }
    free(buf);
}

void tbos_pixbuf_clear(tbos_pixbuf_t* buf, tbos_rgba_t color) {
    if (!buf || !buf->data) return;

    for (uint32_t y = 0; y < buf->height; y++) {
        for (uint32_t x = 0; x < buf->width; x++) {
            tbos_pixbuf_set(buf, x, y, color);
        }
    }
}

tbos_rgba_t tbos_pixbuf_get(const tbos_pixbuf_t* buf, int x, int y) {
    tbos_rgba_t result = {0, 0, 0, 0};
    if (!buf || !buf->data) return result;
    if (x < 0 || x >= (int)buf->width || y < 0 || y >= (int)buf->height) return result;

    uint8_t* row = buf->data + y * buf->stride;

    switch (buf->format) {
        case TBOS_PIXEL_RGBA32: {
            uint8_t* p = row + x * 4;
            result.r = p[0];
            result.g = p[1];
            result.b = p[2];
            result.a = p[3];
            break;
        }
        case TBOS_PIXEL_RGB24: {
            uint8_t* p = row + x * 3;
            result.r = p[0];
            result.g = p[1];
            result.b = p[2];
            result.a = 255;
            break;
        }
        case TBOS_PIXEL_RGB565: {
            uint16_t* p = (uint16_t*)(row + x * 2);
            uint16_t v = *p;
            result.r = ((v >> 11) & 0x1F) << 3;
            result.g = ((v >> 5) & 0x3F) << 2;
            result.b = (v & 0x1F) << 3;
            result.a = 255;
            break;
        }
        case TBOS_PIXEL_8BIT: {
            uint8_t v = row[x];
            result.r = result.g = result.b = v;
            result.a = 255;
            break;
        }
        case TBOS_PIXEL_1BIT: {
            int byte_idx = x / 8;
            int bit_idx = 7 - (x % 8);
            uint8_t v = (row[byte_idx] >> bit_idx) & 1;
            result.r = result.g = result.b = v ? 255 : 0;
            result.a = 255;
            break;
        }
        default:
            break;
    }

    return result;
}

void tbos_pixbuf_set(tbos_pixbuf_t* buf, int x, int y, tbos_rgba_t color) {
    if (!buf || !buf->data) return;
    if (x < 0 || x >= (int)buf->width || y < 0 || y >= (int)buf->height) return;

    uint8_t* row = buf->data + y * buf->stride;

    switch (buf->format) {
        case TBOS_PIXEL_RGBA32: {
            uint8_t* p = row + x * 4;
            p[0] = color.r;
            p[1] = color.g;
            p[2] = color.b;
            p[3] = color.a;
            break;
        }
        case TBOS_PIXEL_RGB24: {
            uint8_t* p = row + x * 3;
            p[0] = color.r;
            p[1] = color.g;
            p[2] = color.b;
            break;
        }
        case TBOS_PIXEL_RGB565: {
            uint16_t* p = (uint16_t*)(row + x * 2);
            *p = ((color.r >> 3) << 11) | ((color.g >> 2) << 5) | (color.b >> 3);
            break;
        }
        case TBOS_PIXEL_8BIT: {
            row[x] = (color.r + color.g + color.b) / 3;  /* Grayscale */
            break;
        }
        case TBOS_PIXEL_1BIT: {
            int byte_idx = x / 8;
            int bit_idx = 7 - (x % 8);
            int brightness = (color.r + color.g + color.b) / 3;
            if (brightness > 127) {
                row[byte_idx] |= (1 << bit_idx);
            } else {
                row[byte_idx] &= ~(1 << bit_idx);
            }
            break;
        }
        default:
            break;
    }
}

void tbos_pixbuf_blend(tbos_pixbuf_t* buf, int x, int y, tbos_rgba_t color) {
    if (!buf || !buf->data) return;
    if (color.a == 255) {
        tbos_pixbuf_set(buf, x, y, color);
        return;
    }
    if (color.a == 0) return;

    tbos_rgba_t dst = tbos_pixbuf_get(buf, x, y);

    /* Alpha blending: result = src * alpha + dst * (1 - alpha) */
    uint16_t alpha = color.a;
    uint16_t inv_alpha = 255 - alpha;

    tbos_rgba_t result;
    result.r = (color.r * alpha + dst.r * inv_alpha) / 255;
    result.g = (color.g * alpha + dst.g * inv_alpha) / 255;
    result.b = (color.b * alpha + dst.b * inv_alpha) / 255;
    result.a = 255;

    tbos_pixbuf_set(buf, x, y, result);
}

/* ═══════════════════════════════════════════════════════════════════════════
 * DRAWING PRIMITIVES
 * ═══════════════════════════════════════════════════════════════════════════ */

void tbos_draw_hline(tbos_pixbuf_t* buf, int x1, int x2, int y, tbos_rgba_t color) {
    if (x1 > x2) { int t = x1; x1 = x2; x2 = t; }
    for (int x = x1; x <= x2; x++) {
        tbos_pixbuf_blend(buf, x, y, color);
    }
}

void tbos_draw_vline(tbos_pixbuf_t* buf, int x, int y1, int y2, tbos_rgba_t color) {
    if (y1 > y2) { int t = y1; y1 = y2; y2 = t; }
    for (int y = y1; y <= y2; y++) {
        tbos_pixbuf_blend(buf, x, y, color);
    }
}

void tbos_draw_line(tbos_pixbuf_t* buf, int x1, int y1, int x2, int y2,
                    tbos_rgba_t color) {
    /* Bresenham's line algorithm */
    int dx = abs(x2 - x1);
    int dy = abs(y2 - y1);
    int sx = x1 < x2 ? 1 : -1;
    int sy = y1 < y2 ? 1 : -1;
    int err = dx - dy;

    while (1) {
        tbos_pixbuf_blend(buf, x1, y1, color);
        if (x1 == x2 && y1 == y2) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x1 += sx; }
        if (e2 < dx) { err += dx; y1 += sy; }
    }
}

void tbos_draw_rect(tbos_pixbuf_t* buf, int x, int y, int w, int h,
                    tbos_rgba_t color) {
    tbos_draw_hline(buf, x, x + w - 1, y, color);
    tbos_draw_hline(buf, x, x + w - 1, y + h - 1, color);
    tbos_draw_vline(buf, x, y, y + h - 1, color);
    tbos_draw_vline(buf, x + w - 1, y, y + h - 1, color);
}

void tbos_draw_rect_fill(tbos_pixbuf_t* buf, int x, int y, int w, int h,
                         tbos_rgba_t color) {
    for (int dy = 0; dy < h; dy++) {
        tbos_draw_hline(buf, x, x + w - 1, y + dy, color);
    }
}

void tbos_draw_circle(tbos_pixbuf_t* buf, int cx, int cy, int r, tbos_rgba_t color) {
    /* Midpoint circle algorithm */
    int x = r, y = 0;
    int err = 0;

    while (x >= y) {
        tbos_pixbuf_blend(buf, cx + x, cy + y, color);
        tbos_pixbuf_blend(buf, cx + y, cy + x, color);
        tbos_pixbuf_blend(buf, cx - y, cy + x, color);
        tbos_pixbuf_blend(buf, cx - x, cy + y, color);
        tbos_pixbuf_blend(buf, cx - x, cy - y, color);
        tbos_pixbuf_blend(buf, cx - y, cy - x, color);
        tbos_pixbuf_blend(buf, cx + y, cy - x, color);
        tbos_pixbuf_blend(buf, cx + x, cy - y, color);

        y++;
        err += 1 + 2 * y;
        if (2 * (err - x) + 1 > 0) {
            x--;
            err += 1 - 2 * x;
        }
    }
}

void tbos_draw_circle_fill(tbos_pixbuf_t* buf, int cx, int cy, int r,
                           tbos_rgba_t color) {
    for (int y = -r; y <= r; y++) {
        int dx = (int)sqrt(r * r - y * y);
        tbos_draw_hline(buf, cx - dx, cx + dx, cy + y, color);
    }
}

void tbos_draw_arc(tbos_pixbuf_t* buf, int cx, int cy, int r,
                   float start_angle, float end_angle, tbos_rgba_t color) {
    int steps = (int)(r * fabs(end_angle - start_angle) / M_PI * 32);
    if (steps < 8) steps = 8;

    float angle_step = (end_angle - start_angle) / steps;
    for (int i = 0; i <= steps; i++) {
        float angle = start_angle + i * angle_step;
        int x = cx + (int)(r * cos(angle));
        int y = cy + (int)(r * sin(angle));
        tbos_pixbuf_blend(buf, x, y, color);
    }
}

void tbos_draw_rect_rounded(tbos_pixbuf_t* buf, int x, int y, int w, int h,
                            int radius, tbos_rgba_t color) {
    if (radius > w / 2) radius = w / 2;
    if (radius > h / 2) radius = h / 2;

    /* Horizontal lines */
    tbos_draw_hline(buf, x + radius, x + w - 1 - radius, y, color);
    tbos_draw_hline(buf, x + radius, x + w - 1 - radius, y + h - 1, color);

    /* Vertical lines */
    tbos_draw_vline(buf, x, y + radius, y + h - 1 - radius, color);
    tbos_draw_vline(buf, x + w - 1, y + radius, y + h - 1 - radius, color);

    /* Corner arcs */
    tbos_draw_arc(buf, x + radius, y + radius, radius, M_PI, 1.5 * M_PI, color);
    tbos_draw_arc(buf, x + w - 1 - radius, y + radius, radius, 1.5 * M_PI, 2 * M_PI, color);
    tbos_draw_arc(buf, x + radius, y + h - 1 - radius, radius, 0.5 * M_PI, M_PI, color);
    tbos_draw_arc(buf, x + w - 1 - radius, y + h - 1 - radius, radius, 0, 0.5 * M_PI, color);
}

void tbos_draw_rect_rounded_fill(tbos_pixbuf_t* buf, int x, int y, int w, int h,
                                  int radius, tbos_rgba_t color) {
    if (radius > w / 2) radius = w / 2;
    if (radius > h / 2) radius = h / 2;

    /* Main body */
    tbos_draw_rect_fill(buf, x + radius, y, w - 2 * radius, h, color);
    tbos_draw_rect_fill(buf, x, y + radius, radius, h - 2 * radius, color);
    tbos_draw_rect_fill(buf, x + w - radius, y + radius, radius, h - 2 * radius, color);

    /* Corners */
    for (int dy = 0; dy < radius; dy++) {
        int dx = (int)sqrt(radius * radius - (radius - dy) * (radius - dy));
        /* Top-left */
        tbos_draw_hline(buf, x + radius - dx, x + radius, y + dy, color);
        /* Top-right */
        tbos_draw_hline(buf, x + w - 1 - radius, x + w - 1 - radius + dx, y + dy, color);
        /* Bottom-left */
        tbos_draw_hline(buf, x + radius - dx, x + radius, y + h - 1 - dy, color);
        /* Bottom-right */
        tbos_draw_hline(buf, x + w - 1 - radius, x + w - 1 - radius + dx, y + h - 1 - dy, color);
    }
}

/* ═══════════════════════════════════════════════════════════════════════════
 * SACRED GEOMETRY
 * ═══════════════════════════════════════════════════════════════════════════ */

void tbos_draw_ashoka_chakra(tbos_pixbuf_t* buf, int cx, int cy, int size,
                              tbos_rgba_t color) {
    int r = size / 2;

    /* Outer circle */
    tbos_draw_circle(buf, cx, cy, r, color);

    /* Inner circle */
    tbos_draw_circle(buf, cx, cy, r / 3, color);

    /* 24 spokes */
    for (int i = 0; i < 24; i++) {
        float angle = i * 2 * M_PI / 24;
        int x1 = cx + (int)((r / 3) * cos(angle));
        int y1 = cy + (int)((r / 3) * sin(angle));
        int x2 = cx + (int)(r * cos(angle));
        int y2 = cy + (int)(r * sin(angle));
        tbos_draw_line(buf, x1, y1, x2, y2, color);
    }
}

void tbos_draw_karma_meter(tbos_pixbuf_t* buf, int x, int y, int w, int h,
                           int karma, int max_karma) {
    /* Background */
    tbos_draw_rect_rounded_fill(buf, x, y, w, h, 4, TBOS_COLOR_BG_PANEL);
    tbos_draw_rect_rounded(buf, x, y, w, h, 4, TBOS_COLOR_BORDER);

    /* Calculate fill width */
    float ratio = (float)karma / max_karma;
    if (ratio > 1.0f) ratio = 1.0f;
    if (ratio < 0.0f) ratio = 0.0f;
    int fill_w = (int)((w - 4) * ratio);

    /* Color based on karma level */
    tbos_rgba_t fill_color;
    if (karma < 0) {
        fill_color = TBOS_COLOR_RED;
    } else if (ratio < 0.3f) {
        fill_color = TBOS_COLOR_YELLOW;
    } else if (ratio < 0.7f) {
        fill_color = TBOS_COLOR_CYAN;
    } else {
        fill_color = TBOS_COLOR_GREEN_UI;
    }

    /* Fill bar */
    if (fill_w > 0) {
        tbos_draw_rect_rounded_fill(buf, x + 2, y + 2, fill_w, h - 4, 2, fill_color);
    }
}

void tbos_draw_consciousness(tbos_pixbuf_t* buf, int cx, int cy, int size,
                             int level) {
    /* Draw concentric circles representing consciousness levels */
    /* Level 0 = dormant (1 circle), Level 4 = enlightened (all 5 circles) */

    tbos_rgba_t colors[] = {
        TBOS_COLOR_MULADHARA,   /* Level 0: Red (root) */
        TBOS_COLOR_MANIPURA,    /* Level 1: Yellow (solar) */
        TBOS_COLOR_ANAHATA,     /* Level 2: Green (heart) */
        TBOS_COLOR_AJNA,        /* Level 3: Indigo (third eye) */
        TBOS_COLOR_SAHASRARA,   /* Level 4: Violet (crown) */
    };

    int r = size / 2;
    int r_step = r / 5;

    for (int i = 0; i <= level && i < 5; i++) {
        int cr = r - i * r_step;
        tbos_draw_circle(buf, cx, cy, cr, colors[i]);
    }

    /* Center dot */
    tbos_draw_circle_fill(buf, cx, cy, r_step / 2,
                          level >= 4 ? TBOS_COLOR_WHITE : colors[level]);
}

